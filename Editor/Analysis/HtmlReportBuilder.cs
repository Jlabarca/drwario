        public static string Build(DiagnosticReport report, ProfilingSession session)
        {
            var sb = new StringBuilder();

            var date = report.GeneratedAt.ToString("yyyy-MM-dd HH:mm:ss");
            var duration = (report.Session.EndTime - report.Session.StartTime).TotalSeconds;

            sb.Append("<!DOCTYPE html>\n<html lang=\"en\">\n<head>\n<meta charset=\"utf-8\">\n<title>DrWario Report - ");
            sb.Append(Esc(date));
            sb.Append("</title>\n");
            AppendStyles(sb);
            sb.Append("</head>\n<body>\n");

            // Header
            sb.Append("<div class=\"header\">\n");
            sb.Append("<h1>DrWario Diagnostic Report</h1>\n");
            sb.Append("<table class=\"meta-table\">\n");
            MetaRow(sb, "Generated", date);
            MetaRow(sb, "Platform", Esc(report.Session.Platform));
            MetaRow(sb, "Unity Version", Esc(report.Session.UnityVersion));
            MetaRow(sb, "Duration", $"{duration:F1}s");
            MetaRow(sb, "Environment", report.Session.IsEditor ? "Editor" : "Build");
            sb.Append("</table>\n</div>\n");

            // Grade summary
            AppendGradeSummary(sb, report);

            // Metrics table
            AppendMetricsTable(sb, report);

            // SVG charts
            var frames = session.GetFrames();
            if (frames.Length > 0)
            {
                AppendFrameTimeChart(sb, frames, report.Session.TargetFrameRate);
                AppendMemoryChart(sb, frames);
            }

            // Synthesis (deterministic insights)
            AppendSynthesis(sb, report);

            // Findings
            AppendFindings(sb, report);

            // Footer
            sb.Append("<div class=\"footer\">Generated by DrWario v3.0</div>\n");
            sb.Append("</body>\n</html>");

            return sb.ToString();
        }

