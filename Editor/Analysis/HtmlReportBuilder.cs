        private static void MetaRow(StringBuilder sb, string label, string value)
        {
            sb.Append("<tr><td>").Append(Esc(label)).Append("</td><td>").Append(value).Append("</td></tr>\n");
        }

        private static void AppendGradeSummary(StringBuilder sb, DiagnosticReport report)
        {
            sb.Append("<div class=\"grade-section\">\n");
            sb.Append("<div class=\"grade-overall grade-").Append(report.OverallGrade).Append("\">");
            sb.Append(report.OverallGrade);
            sb.Append("</div>\n");
            sb.Append("<div class=\"grade-score\">").Append($"{report.HealthScore:F0} / 100").Append("</div>\n");

            if (report.CategoryGrades.Count > 0)
            {
                sb.Append("<div class=\"category-grades\">\n");
                foreach (var kv in report.CategoryGrades)
                {
                    sb.Append("<div class=\"cat-grade\">");
                    sb.Append("<div class=\"cat-grade-letter grade-").Append(kv.Value).Append("\">").Append(kv.Value).Append("</div>");
                    sb.Append("<div class=\"cat-grade-label\">").Append(Esc(kv.Key)).Append("</div>");
                    sb.Append("</div>\n");
                }
                sb.Append("</div>\n");
            }

            sb.Append("</div>\n");
        }

        private static void AppendMetricsTable(StringBuilder sb, DiagnosticReport report)
        {
            sb.Append("<h2>Performance Metrics</h2>\n");
            sb.Append("<table class=\"metrics\">\n");
            sb.Append("<tr><th>Metric</th><th>Value</th></tr>\n");
            MRow(sb, "Avg CPU Time", $"{report.AvgCpuTimeMs:F2} ms");
            MRow(sb, "P95 CPU Time", $"{report.P95CpuTimeMs:F2} ms");
            MRow(sb, "P99 CPU Time", $"{report.P99CpuTimeMs:F2} ms");
            MRow(sb, "Avg GC Alloc", $"{report.AvgGcAllocBytes:F0} bytes/frame");
            MRow(sb, "Avg Draw Calls", $"{report.AvgDrawCalls:F0}");
            MRow(sb, "Memory Slope", $"{report.MemorySlope:F2} bytes/frame");
            sb.Append("</table>\n");
        }

        private static void MRow(StringBuilder sb, string metric, string value)
        {
            sb.Append("<tr><td>").Append(Esc(metric)).Append("</td><td>").Append(Esc(value)).Append("</td></tr>\n");
        }

        private static void AppendSynthesis(StringBuilder sb, DiagnosticReport report)
        {
            if (!report.Synthesis.HasValue) return;
            var syn = report.Synthesis.Value;

            sb.Append("<div class=\"synthesis\">\n");

            if (!string.IsNullOrEmpty(syn.ExecutiveSummary))
            {
                sb.Append("<h2>Summary</h2>\n");
                sb.Append("<p class=\"executive-summary\">").Append(Esc(syn.ExecutiveSummary)).Append("</p>\n");
            }

            if (syn.Correlations != null && syn.Correlations.Count > 0)
            {
                sb.Append("<h3>Insights</h3>\n");
                foreach (var c in syn.Correlations)
                {
                    string sevClass = c.Severity == Severity.Critical ? "critical"
                        : c.Severity == Severity.Warning ? "warning" : "info";
                    sb.Append("<div class=\"finding-card ").Append(sevClass).Append("\">\n");
                    sb.Append("<div class=\"finding-title\">").Append(Esc(c.Title)).Append("</div>\n");
                    sb.Append("<div class=\"finding-desc\">").Append(Esc(c.Description)).Append("</div>\n");
                    sb.Append("<div class=\"finding-rec\">").Append(Esc(c.Recommendation)).Append("</div>\n");
                    sb.Append("</div>\n");
                }
            }

            if (syn.PrioritizedActions != null && syn.PrioritizedActions.Count > 0)
            {
                sb.Append("<h3>Prioritized Actions</h3>\n<ol>\n");
                foreach (var a in syn.PrioritizedActions)
                {
                    sb.Append("<li><strong>[").Append(Esc(a.ExpectedImpact)).Append("]</strong> ");
                    sb.Append(Esc(a.Action)).Append("</li>\n");
                }
                sb.Append("</ol>\n");
            }

            sb.Append("</div>\n");
        }

        private static void AppendFindings(StringBuilder sb, DiagnosticReport report)
        {
            sb.Append("<h2>Findings</h2>\n");

            var sorted = report.Findings.OrderByDescending(f => f.Severity).ToList();
            if (sorted.Count == 0)
            {
                sb.Append("<p>No findings detected. Your project looks healthy!</p>\n");
                return;
            }

            foreach (var f in sorted)
            {
                var sev = f.Severity.ToString();
                sb.Append("<div class=\"finding-card severity-").Append(sev).Append("\">\n");

                // Title line with badges
                sb.Append("<div class=\"finding-title\">");
                sb.Append("<span class=\"badge badge-").Append(sev).Append("\">").Append(sev).Append("</span>");
                sb.Append(Esc(f.Title));
                if (f.Confidence != Confidence.High)
                    sb.Append("<span class=\"badge-confidence\">").Append(f.Confidence).Append(" confidence</span>");
                sb.Append("</div>\n");

                // Description
                sb.Append("<div class=\"finding-desc\">").Append(Esc(f.Description)).Append("</div>\n");

                // Recommendation
                if (!string.IsNullOrEmpty(f.Recommendation))
                    sb.Append("<div class=\"finding-rec\">").Append(Esc(f.Recommendation)).Append("</div>\n");

                // Metadata
                var metaParts = new List<string>();
                if (f.Metric != 0 || f.Threshold != 0)
                    metaParts.Add($"Metric: {f.Metric:F2} (threshold: {f.Threshold:F2})");
                if (!string.IsNullOrEmpty(f.ScriptPath))
                {
                    var script = f.ScriptPath;
                    if (f.ScriptLine > 0) script += $":{f.ScriptLine}";
                    metaParts.Add($"Script: {script}");
                }
                if (!string.IsNullOrEmpty(f.AssetPath))
                    metaParts.Add($"Asset: {f.AssetPath}");
                if (!string.IsNullOrEmpty(f.EnvironmentNote))
                    metaParts.Add(f.EnvironmentNote);

                if (metaParts.Count > 0)
                {
                    sb.Append("<div class=\"finding-meta\">");
                    sb.Append(Esc(string.Join(" | ", metaParts)));
                    sb.Append("</div>\n");
                }

                sb.Append("</div>\n");
            }
        }

        private static void AppendFrameTimeChart(StringBuilder sb, FrameSample[] frames, int targetFrameRate)
        {
            const int svgW = 600;
            const int svgH = 200;
            const int padL = 50;
            const int padR = 10;
            const int padT = 20;
            const int padB = 30;
            int plotW = svgW - padL - padR;
            int plotH = svgH - padT - padB;

            var values = Downsample(frames.Select(f => (double)f.CpuFrameTimeMs).ToArray(), 200);
            if (values.Length == 0) return;

            double maxVal = values.Max();
            float targetMs = targetFrameRate > 0 ? 1000f / targetFrameRate : 16.67f;
            if (maxVal < targetMs * 1.2) maxVal = targetMs * 1.5;

            sb.Append("<div class=\"chart-container\">\n<h2>Frame Time (CPU)</h2>\n");
            sb.Append($"<svg class=\"chart\" viewBox=\"0 0 {svgW} {svgH}\" preserveAspectRatio=\"xMidYMid meet\">\n");

            // Grid lines
            AppendGridLines(sb, padL, padT, plotW, plotH, maxVal, "ms");

            // Target frame time reference line
            double targetY = padT + plotH - (targetMs / maxVal * plotH);
            sb.Append($"<line x1=\"{padL}\" y1=\"{F(targetY)}\" x2=\"{padL + plotW}\" y2=\"{F(targetY)}\" stroke=\"#F44336\" stroke-width=\"1\" stroke-dasharray=\"4,4\" />\n");
            sb.Append($"<text x=\"{padL + plotW + 2}\" y=\"{F(targetY + 4)}\" fill=\"#F44336\" font-size=\"9\">{targetMs:F1}ms</text>\n");

            // Polyline
            var points = new StringBuilder();
            for (int i = 0; i < values.Length; i++)
            {
                double x = padL + (double)i / (values.Length - 1) * plotW;
                double y = padT + plotH - (values[i] / maxVal * plotH);
                if (i > 0) points.Append(' ');
                points.Append(F(x)).Append(',').Append(F(y));
            }
            sb.Append($"<polyline points=\"{points}\" fill=\"none\" stroke=\"#4CAF50\" stroke-width=\"1.5\" />\n");

            // X axis label
            sb.Append($"<text x=\"{svgW / 2}\" y=\"{svgH - 2}\" fill=\"#888\" font-size=\"10\" text-anchor=\"middle\">Frames</text>\n");

            sb.Append("</svg>\n</div>\n");
        }

        private static void AppendMemoryChart(StringBuilder sb, FrameSample[] frames)
        {
            const int svgW = 600;
            const int svgH = 200;
            const int padL = 60;
            const int padR = 10;
            const int padT = 20;
            const int padB = 30;
            int plotW = svgW - padL - padR;
            int plotH = svgH - padT - padB;

            var rawValues = frames.Select(f => (double)f.TotalHeapBytes).ToArray();
            // Skip if all zeros
            if (rawValues.All(v => v == 0)) return;

            var values = Downsample(rawValues, 200);
            if (values.Length == 0) return;

            double maxVal = values.Max();
            double minVal = values.Min();
            if (maxVal <= minVal) maxVal = minVal + 1;

            // Use MB for axis labels
            double range = maxVal - minVal;

            sb.Append("<div class=\"chart-container\">\n<h2>Heap Memory</h2>\n");
            sb.Append($"<svg class=\"chart\" viewBox=\"0 0 {svgW} {svgH}\" preserveAspectRatio=\"xMidYMid meet\">\n");

            // Grid lines (memory-specific)
            int gridLines = 4;
            for (int i = 0; i <= gridLines; i++)
            {
                double frac = (double)i / gridLines;
                double y = padT + plotH - frac * plotH;
                double val = minVal + frac * range;
                string label = FormatBytes(val);
                sb.Append($"<line x1=\"{padL}\" y1=\"{F(y)}\" x2=\"{padL + plotW}\" y2=\"{F(y)}\" stroke=\"#2a2a4a\" stroke-width=\"1\" />\n");
                sb.Append($"<text x=\"{padL - 4}\" y=\"{F(y + 4)}\" fill=\"#888\" font-size=\"9\" text-anchor=\"end\">{label}</text>\n");
            }

            // Polyline
            var points = new StringBuilder();
            for (int i = 0; i < values.Length; i++)
            {
                double x = padL + (double)i / (values.Length - 1) * plotW;
                double y = padT + plotH - ((values[i] - minVal) / range * plotH);
                if (i > 0) points.Append(' ');
                points.Append(F(x)).Append(',').Append(F(y));
            }
            sb.Append($"<polyline points=\"{points}\" fill=\"none\" stroke=\"#2196F3\" stroke-width=\"1.5\" />\n");

            // X axis label
            sb.Append($"<text x=\"{svgW / 2}\" y=\"{svgH - 2}\" fill=\"#888\" font-size=\"10\" text-anchor=\"middle\">Frames</text>\n");

            sb.Append("</svg>\n</div>\n");
        }

        private static void AppendGridLines(StringBuilder sb, int padL, int padT, int plotW, int plotH, double maxVal, string unit)
        {
            int gridLines = 4;
            for (int i = 0; i <= gridLines; i++)
            {
                double frac = (double)i / gridLines;
                double y = padT + plotH - frac * plotH;
                double val = frac * maxVal;
                sb.Append($"<line x1=\"{padL}\" y1=\"{F(y)}\" x2=\"{padL + plotW}\" y2=\"{F(y)}\" stroke=\"#2a2a4a\" stroke-width=\"1\" />\n");
                sb.Append($"<text x=\"{padL - 4}\" y=\"{F(y + 4)}\" fill=\"#888\" font-size=\"9\" text-anchor=\"end\">{val:F1}{unit}</text>\n");
            }
        }

        private static double[] Downsample(double[] data, int maxPoints)
        {
            if (data.Length <= maxPoints) return data;

            var result = new double[maxPoints];
            double step = (double)data.Length / maxPoints;
            for (int i = 0; i < maxPoints; i++)
            {
                int start = (int)(i * step);
                int end = (int)((i + 1) * step);
                if (end > data.Length) end = data.Length;
                if (end <= start) end = start + 1;

                // Take the max in each bucket to preserve spikes
                double max = data[start];
                for (int j = start + 1; j < end; j++)
                {
                    if (data[j] > max) max = data[j];
                }
                result[i] = max;
            }
            return result;
        }

        private static string FormatBytes(double bytes)
        {
            if (bytes >= 1024 * 1024 * 1024) return $"{bytes / (1024 * 1024 * 1024):F1} GB";
            if (bytes >= 1024 * 1024) return $"{bytes / (1024 * 1024):F1} MB";
            if (bytes >= 1024) return $"{bytes / 1024:F0} KB";
            return $"{bytes:F0} B";
        }

        private static string F(double value)
        {
            return value.ToString("F1", CultureInfo.InvariantCulture);
        }

        private static string Esc(string text)
        {
            if (string.IsNullOrEmpty(text)) return "";
            return text
                .Replace("&", "&amp;")
                .Replace("<", "&lt;")
                .Replace(">", "&gt;")
                .Replace("\"", "&quot;");
        }
